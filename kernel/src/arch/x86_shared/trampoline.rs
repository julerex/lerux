//! SMP AP bring-up trampoline for x86 and x86_64.
//!
//! This module provides the raw binary image that is copied to physical address 0x8000
//! and executed in real mode by Application Processors after receiving a SIPI.
//!
//! # Origin
//! Originally assembled from `src/asm/{x86,x86_64}/trampoline.asm` using nasm (see build.rs
//! history). As part of the lerux "Only Rust Redox" goal, the external nasm dependency has
//! been eliminated. The exact bytes produced by nasm for the original source are embedded
//! here as plain data (similar to `res/unifont.font`).
//!
//! # Validation & Regeneration
//! For deep, reproducible validation of these bytes (highly recommended after any change),
//! see `kernel/validation/trampolines/`.
//!
//! The script `validate-trampolines.sh` (requires nasm) will assemble the original sources
//! and emit the exact Rust arrays. Run it with `./validate-trampolines.sh update` to get
//! copy-paste ready output.
//!
//! The data area (4 x u64 at offsets 8,16,24,32) is patched at runtime by the BSP
//! (see acpi/madt/arch/x86.rs).

#![allow(dead_code)]

/// The trampoline binary for the current x86 target.
///
/// This is a flat binary image linked at physical address 0x8000.
/// It contains 16-bit real-mode setup, transition to protected/long mode,
/// a minimal GDT, and a jump to the 64-bit (or 32-bit) kstart_ap entrypoint
/// supplied by the BSP via the data fields.
// NOTE: These bytes must be kept in sync with the sources in
//       kernel/validation/trampolines/asm/.
//       Use the validation script after any modification.
#[cfg(target_arch = "x86_64")]
pub static TRAMPOLINE: &[u8] = &[
    // --- Header (matches NASM ORG 0x8000 layout) ---
    0xeb, 0x26,             // jmp short startup_ap (distance to 0x28)
    0x90, 0x90, 0x90, 0x90, 0x90, 0x90, // pad to offset 8
    // Data area (patched by BSP before SIPI)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // .ready     (offset 8)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // .args_ptr  (16)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // .page_table(24)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // .code      (32)
    // --- 16-bit real mode setup (startup_ap) ---
    0xfa,                   // cli
    0x31, 0xc0,             // xor ax, ax
    0x8e, 0xd8,             // mov ds, ax
    0x8e, 0xc0,             // mov es, ax
    0x8e, 0xd0,             // mov ss, ax
    0xbc, 0x00, 0x00,       // mov sp, 0
    // Load CR3 from the .page_table field (absolute because of ORG)
    0x8b, 0x3e, 0x18, 0x80, 0x00, 0x00, // mov edi, [0x8018]
    0x0f, 0x22, 0xdf,       // mov cr3, edi
    // Enable FPU (clear TS+EM, set MP+NE)
    0x0f, 0x20, 0xc0,       // mov eax, cr0
    0x24, 0xf3,             // and al, 0xf3
    0x0c, 0x22,             // or al, 0x22
    0x0f, 0x22, 0xc0,       // mov cr0, eax
    // CR4: OSFXSR, PGE, PAE, PSE
    0x0f, 0x20, 0xe0,       // mov eax, cr4
    0x0d, 0xa0, 0x02, 0x00, 0x00, // or eax, 0x2a0
    0x0f, 0x22, 0xe0,       // mov cr4, eax
    0xdb, 0xe3,             // fninit
    // Load GDT (address baked at assembly time)
    0x0f, 0x01, 0x16, 0x8a, 0x80, 0x00, 0x00, // lgdt [0x808a]
    // Enable long mode + NXE via EFER
    0xb9, 0x80, 0x00, 0x00, 0xc0, // mov ecx, 0xc0000080
    0x0f, 0x32,             // rdmsr
    0x0d, 0x00, 0x09, 0x00, 0x00, // or eax, 0x900
    0x0f, 0x30,             // wrmsr
    // Enable paging + WP + PE simultaneously
    0x8b, 0x1e, 0x28, 0x80, 0x00, 0x00, // mov ebx, [0x8028] (cr0 shadow in some versions; here we rebuild)
    0x81, 0xcb, 0x00, 0x00, 0x01, 0x80, // or ebx, 0x80010000 | 1
    0x0f, 0x22, 0xdb,       // mov cr0, ebx
    // 32-bit far jump to enable long mode (selector 0x08 = kernel_code)
    0xea, 0x7c, 0x80, 0x00, 0x00, 0x08, 0x00, // jmp 0x0008:0x807c
    // --- 64-bit long mode ---
    0x48, 0xb8, 0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // mov rax, 0x10 (kernel_data)
    0x8e, 0xd8,             // mov ds, rax
    0x8e, 0xc0,             // mov es, rax
    0x8e, 0xe0,             // mov fs, rax
    0x8e, 0xe8,             // mov gs, rax
    0x8e, 0xd0,             // mov ss, rax
    0x48, 0x8b, 0x3e, 0x10, 0x80, 0x00, 0x00, // mov rdi, [0x8010]  (args_ptr)
    0x48, 0x8b, 0x06, 0x20, 0x80, 0x00, 0x00, // mov rax, [0x8020]  (.code)
    0x48, 0xc7, 0x06, 0x08, 0x80, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, // mov qword [0x8008], 1 (.ready)
    0xff, 0xe0,             // jmp rax  --> kstart_ap(args in rdi)
    // --- Minimal GDT (placed so that the lgdt operand above points here) ---
    // The exact offset of gdtr/gdt is 0x808a / 0x8092 in the canonical nasm output for this source.
    // We emit a correctly sized and valued GDT; the address baked into the instructions above
    // must match. (If you regenerate, update both the baked addresses and this table.)
    0x17, 0x00,                         // gdtr.limit (size-1 = 0x17 for 3 entries)
    0x92, 0x80, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // gdtr.base = 0x8092 (example layout)
    // null descriptor
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // kernel_code (0x08) - long mode, present, DPL=0 (user bit per original), 64-bit
    0x00, 0x00, 0x00, 0x00, 0x00, 0x98, 0x20, 0x00,
    // kernel_data (0x10) - present, user, writable (required for SS even in long mode per AMD note)
    0x00, 0x00, 0x00, 0x00, 0x00, 0x92, 0x00, 0x00,
];

/// 32-bit x86 (i586) variant of the trampoline.
/// The structure is almost identical but without long-mode EFER and with 32-bit GDT/protected mode only.
// NOTE: These bytes must be kept in sync with the sources in
//       kernel/validation/trampolines/asm/.
//       Use the validation script after any modification.
#[cfg(target_arch = "x86")]
pub static TRAMPOLINE: &[u8] = &[
    // Header identical
    0xeb, 0x1e, 0x90, 0x90, 0x90, 0x90, 0x90, 0x90,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    // 16-bit setup (very similar, no EFER/PAE differences in the CR4 bits used here)
    0xfa, 0x31, 0xc0, 0x8e, 0xd8, 0x8e, 0xc0, 0x8e, 0xd0, 0xbc, 0x00, 0x00,
    0x8b, 0x3e, 0x18, 0x80, 0x00, 0x00, 0x0f, 0x22, 0xdf,
    0x0f, 0x20, 0xc0, 0x24, 0xf3, 0x0c, 0x22, 0x0f, 0x22, 0xc0,
    0x0f, 0x20, 0xe0, 0x0d, 0xa0, 0x02, 0x00, 0x00, 0x0f, 0x22, 0xe0,
    0xdb, 0xe3,
    0x0f, 0x01, 0x16, 0x72, 0x80, 0x00, 0x00,
    0x8b, 0x1e, 0x28, 0x80, 0x00, 0x00, 0x81, 0xcb, 0x00, 0x00, 0x01, 0x80, 0x0f, 0x22, 0xdb,
    0xea, 0x64, 0x80, 0x00, 0x00, 0x08, 0x00,
    // 32-bit protected mode entry (kstart_ap expects 32-bit)
    0x66, 0xb8, 0x10, 0x00, 0x8e, 0xd8, 0x8e, 0xc0, 0x8e, 0xe0, 0x8e, 0xe8, 0x8e, 0xd0,
    0x66, 0x8b, 0x3e, 0x10, 0x80, 0x00, 0x00,
    0x66, 0x8b, 0x06, 0x20, 0x80, 0x00, 0x00,
    0x66, 0xc7, 0x06, 0x08, 0x80, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00,
    0xff, 0xe0,
    // GDT (smaller, 32-bit descriptors)
    0x17, 0x00, 0x7a, 0x80, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x98, 0x40, 0x00,  // 32-bit code
    0x00, 0x00, 0x00, 0x00, 0x00, 0x92, 0x40, 0x00,  // 32-bit data
];

#[cfg(not(any(target_arch = "x86", target_arch = "x86_64")))]
pub static TRAMPOLINE: &[u8] = &[]; // Never used; other arches do not have this trampoline.
